import java.awt.Color;


public class Circle {

    int x;

    int y;

    int size;

    Color color;

    public Circle(int x, int y, int size){

        this.x = x;

        this.y = y;

        this.size = size;

    }

    public void setX(int x){

        this.x = x;

    }

    public void setY(int y){

        this.y = y;

    }

    public void setColor(Color color){

        this.color = color;

    }

}



